            if (!FPath.toString().isEmpty()) {
                FileReader fistream = new FileReader(FPath);

                BufferedReader bfr = new BufferedReader(fistream);
                bfr.readLine();
                while ((Cline = bfr.readLine()) != null) {
                    String s = "";
                    s = Cline;
                    StringTokenizer st = new StringTokenizer(s, ",");
                    while (st.hasMoreTokens()) {
                        String s1 = st.nextToken();
                        String s2 = st.nextToken();
                        String s3 = st.nextToken();
                        String s4 = st.nextToken();
                        String s5 = st.nextToken();
                        jTable1.setValueAt(s1, row, 0);
                        jTable1.setValueAt(s2, row, 1);
                        jTable1.setValueAt(s3, row, 2);
                        jTable1.setValueAt(s4, row, 3);
                        jTable1.setValueAt(s5, row, 4);
                    
                    row++;
                
            
           }
                }
           
                
            
        }
